// call the drawFretboard() function in the console to draw fretboard to screen
var drawFretboard = function() {
  var string1 = fretboard.highEstring;
  var string2 = fretboard.Bstring;
  var string3 = fretboard.Gstring;
  var string4 = fretboard.Dstring;
  var string5 = fretboard.Astring;
  var string6 = fretboard.lowEstring;

  var stringArray = [string1,string2,string3,string4,string5,string6];
  var i=0;

  document.write("<br>" + "--------------------------------------------------------------------------" + "<br>");
  while (i < stringArray.length) {
    for (var note in stringArray[i]) {
      document.write(stringArray[i][note].note + ' | ');
    };
    document.write('<br>');
    i++;
  }
  document.write("--------------------------------------------------------------------------" + "<br>");
}
drawFretboard();
